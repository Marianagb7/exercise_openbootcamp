public class Condicional {

    public  static void main(String[] args){
        int numeroIf = -4;
        if (numeroIf > 0){
            System.out.println("Es un número positivo");
        } else if (numeroIf < 0) {
            System.out.println("Es un número negativo");
        } else {
            System.out.println("El número cero es neutro");
        }
    }
}
